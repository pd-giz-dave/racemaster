'use strict';

import { state }                           from '../state.js';
import { formatResults, computeAvgTop10 } from '../results.js';
import { getEntryName } from '../entries.js';
import { getCategoryPriority }             from '../categories.js';
import { toISODate }           from '../utils.js';
import { sanitise, escHtml }   from '../ui.js';

function wrap(title, body) {
  const org  = escHtml(state.event.organisation || '');
  const name = escHtml(state.event.name         || '');
  const date = escHtml(state.event.date         || '');
  const bannerEvent = [org ? `${org} — ${name}` : name, date].filter(Boolean).join(' · ');
  return `<!DOCTYPE html>
<html lang="en">
<head><meta charset="utf-8"><meta name="viewport" content="width=device-width,initial-scale=1">
<title>${escHtml(title)}</title>
<link rel="icon" href="favicon.ico" type="image/x-icon" sizes="48x48">
<link rel="apple-touch-icon" href="icon-192.png">
<link rel="stylesheet" href="publish.css">
</head>
<body>
<header class="rm-banner">
  <img src="favicon.ico" class="rm-icon" alt="">
  <span class="rm-brand">RaceMaster</span>
  <span class="rm-event">${bannerEvent}</span>
</header>
<div class="re-page">
<div class="re-search-row"><input type="search" id="re-search" placeholder="Search results…" autocomplete="off"></div>
${body}
<p class="re-footer">Generated by RaceMaster on ${new Date().toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' })}</p>
</div>
<script src="publish.js"></script>
</body></html>`;
}


// ---- Shared tbody row builders (used by screen rendering and publish HTML) ----

export function buildSeniorsBodyHTML(results) {
  return results.map(r => {
    const pos  = r.position < 9999 ? r.position : 'DNF';
    const time = r.position < 9999 ? escHtml(r.time || '') + (r.recordBreaker ? ' R' : '') : '';
    return `<tr>
      <td>${pos}</td><td>${escHtml(r.bibNumber||'')}</td>
      <td>${escHtml(r.inCatPos||'')}</td>
      <td>${escHtml(getEntryName(r))}</td><td>${escHtml(r.club||'')}</td>
      <td>${escHtml(r.category||'')}</td><td>${time}</td>
      <td>${r.pctLdrs ? r.pctLdrs + '%' : ''}</td><td>${escHtml(r.behindTime||'')}</td></tr>`;
  }).join('');
}

export function buildJuniorsBodyHTML(results) {
  const groups = new Map();
  for (const r of results) {
    const cat = r.category || '';
    if (!groups.has(cat)) groups.set(cat, []);
    groups.get(cat).push(r);
  }
  const sortedCats = [...groups.keys()].sort((a, b) => getCategoryPriority(a) - getCategoryPriority(b));
  for (const cat of sortedCats) {
    groups.get(cat).sort((a, b) => {
      if (a.position >= 9999 && b.position < 9999) return 1;
      if (a.position < 9999 && b.position >= 9999) return -1;
      return a.position - b.position;
    });
  }
  const rows = [];
  sortedCats.forEach((cat, i) => {
    if (i > 0) rows.push(`<tr class="results-cat-separator"><td colspan="6"></td></tr>`);
    for (const r of groups.get(cat)) {
      const time = r.position < 9999 ? escHtml(r.time || '') : 'DNF';
      rows.push(`<tr>
        <td>${escHtml(r.bibNumber||'')}</td>
        <td>${escHtml(r.inCatPos||'')}</td><td>${escHtml(getEntryName(r))}</td>
        <td>${escHtml(r.club||'')}</td><td>${escHtml(r.category||'')}</td>
        <td>${time}</td></tr>`);
    }
  });
  return rows.join('');
}

export function buildPairsBodyHTML(pairsResults) {
  return pairsResults.map(r => {
    const pos   = r.position < 9999 ? r.position : 'DNF';
    const time  = r.position < 9999 ? escHtml(r.time || '') : 'DNF';
    const club2 = r.partner?.club;
    const club  = club2 && club2 !== r.club ? `${r.club || ''} / ${club2}` : (r.club || '');
    const pg    = r.pairGender || '';
    const cat   = pg ? `${r.category || ''} ${pg}`.trim() : (r.category || '');
    return `<tr>
      <td>${pos}</td><td>${escHtml(r.bibNumber||'')}</td><td>${r.inCatPos||''}</td>
      <td>${escHtml(r.name||'')}</td><td>${escHtml(r.partner?.name||'')}</td>
      <td>${escHtml(club)}</td><td>${escHtml(cat)}</td><td>${time}</td></tr>`;
  }).join('');
}

export function buildHelpersBodyHTML(helpersReport) {
  return helpersReport.map(h => `<tr>
    <td>${escHtml(h.role||'')}</td><td>${escHtml(h.name||'')}</td>
    <td>${escHtml(h.club||'')}</td><td>${escHtml(h.cat||'')}</td>
    <td>${escHtml(h.lastRaced||'')}</td></tr>`).join('');
}

// ---- Publish section builders (wrap row HTML in full table markup) ----

function section(label, count, content) {
  return `<details class="re-section-details"><summary>${label} (${count})</summary>${content}</details>`;
}

function juniorsSection(juniors) {
  if (!juniors.length) return '';
  return section('Junior Results', juniors.length, `<table class="data-table re-sortable">
  <thead><tr><th>Bib</th><th>In Cat</th><th>Name</th><th>Club</th><th>Cat</th><th>Time</th></tr></thead>
  <tbody>${buildJuniorsBodyHTML(juniors)}</tbody>
</table>`);
}

function seniorsSection(seniors) {
  if (!seniors.length) return '';
  const avg = computeAvgTop10(seniors);
  const n   = Math.min(seniors.filter(r => r.position < 9999).length, 10);
  const note = avg ? `Top ${n} average: ${avg} &nbsp; R = course record` : 'R = course record';
  return section('Senior Results', seniors.length, `<p class="re-summary">${note}</p><table class="data-table re-sortable">
  <thead><tr><th>Pos</th><th>Bib</th><th>In Cat</th><th>Name</th><th>Club</th><th>Cat</th><th>Time</th><th>%Ldrs</th><th>Behind</th></tr></thead>
  <tbody>${buildSeniorsBodyHTML(seniors)}</tbody>
</table>`);
}

function pairsSection(pairsResults) {
  if (!pairsResults.length) return '';
  return section('Pairs Results', pairsResults.length, `<table class="data-table re-sortable">
  <thead><tr><th>Pos</th><th>Bib</th><th>In Cat</th><th>Name</th><th>Partner</th><th>Club</th><th>Cat</th><th>Time</th></tr></thead>
  <tbody>${buildPairsBodyHTML(pairsResults)}</tbody>
</table>`);
}

function helpersSection(helpersReport) {
  if (!helpersReport.length) return '';
  return section('Helpers', helpersReport.length, `<table class="data-table re-sortable">
  <thead><tr><th>Role</th><th>Name</th><th>Club</th><th>Cat</th><th>Last Raced</th></tr></thead>
  <tbody>${buildHelpersBodyHTML(helpersReport)}</tbody>
</table>`);
}

const SECTIONS = {
  combined: ({ seniors, juniors, pairsResults, helpersReport }) =>
    juniorsSection(juniors) + seniorsSection(seniors) + pairsSection(pairsResults) + helpersSection(helpersReport),
  juniors:  ({ juniors })         => juniorsSection(juniors),
  seniors:  ({ seniors })         => seniorsSection(seniors),
  helpers:  ({ helpersReport })   => helpersSection(helpersReport),
  pairs:    ({ pairsResults })    => pairsSection(pairsResults),
};

const TITLES = {
  combined: () => state.event.name || 'Results',
  juniors:  () => `${state.event.name || 'Results'} — Juniors`,
  seniors:  () => `${state.event.name || 'Results'} — Seniors`,
  helpers:  () => `${state.event.name || 'Results'} — Helpers`,
  pairs:    () => `${state.event.name || 'Results'} — Pairs`,
};

function makeFilename() {
  const name = sanitise(state.event.name || 'results');
  const date = toISODate(state.event.date || '');
  return date ? `${name}_${date}_results.html` : `${name}_results.html`;
}

export function makePublishedUrl() {
  return `/results/${makeFilename()}`;
}

export async function publishResultsHTML() {
  const data     = formatResults();
  const filename = makeFilename();
  const html     = wrap(TITLES.combined(), SECTIONS.combined(data));
  const token    = localStorage.getItem('racemaster-token');
  const res = await fetch('/api/publish-results', {
    method:  'POST',
    headers: { 'Content-Type': 'application/json', 'Authorization': `Bearer ${token}` },
    body:    JSON.stringify({ filename, html, copy: [
      { src: 'js/publish/publish.css', name: 'publish.css' },
      { src: 'js/publish/publish.js',  name: 'publish.js'  },
      { src: 'favicon.ico',            name: 'favicon.ico'  },
      { src: 'icon-192.png',           name: 'icon-192.png' },
    ]}),
  });
  if (!res.ok) {
    const err = await res.json().catch(() => ({}));
    throw new Error(err.error || `Server error ${res.status}`);
  }
  const { url } = await res.json();
  return url;
}